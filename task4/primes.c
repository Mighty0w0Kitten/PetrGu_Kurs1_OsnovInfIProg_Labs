#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>

// Объявления функций
unsigned int get_input();
void calculate_primes(int primes[], int n);

// Главная функция программы
int main() {
    // Получаем значение n от пользователя
    unsigned int n = get_input();

    // Выделяем память для массива индикаторов простоты чисел
    int *primes = malloc((n + 1) * sizeof(int));
    if (!primes) {
        fprintf(stderr, "Ошибка: не удалось выделить память.\n");
        return EXIT_FAILURE;
    }

    // Выполняем построение массива индикаторов простоты чисел
    calculate_primes(primes, n);

    // Выводим найденные простые числа
    printf("Простые числа от 1 до %u:\n", n);
    for (unsigned int i = 2; i <= n; i++) {
        if (primes[i]) { // Если число помечено как простое
            printf("%u ", i);
        }
    }
    printf("\n");

    // Освобождаем выделенную память
    free(primes);

    return 0; // Завершаем программу с кодом успешного завершения
}

// Функция для получения значения n
// Эта функция запрашивает у пользователя число n и проверяет его корректность.
// Если введенное число не удовлетворяет условиям (1 <= n <= 10,000,000),
// программа просит повторить ввод, пока не будет получено корректное значение.
unsigned int get_input() {
    unsigned int n;
    printf("Введите значение n (1 <= n <= 10000000): ");
    while (scanf("%u", &n) != 1 || n < 1 || n > 10000000) {
        printf("Неверный ввод. Попробуйте снова: ");
        while (getchar() != '\n'); // Очистка буфера ввода для предотвращения зацикливания
    }
    return n;
}

// Функция для построения массива индикаторов простых чисел
// Эта функция реализует Решето Эратосфена для нахождения всех простых чисел до n.
// primes[] - массив, в котором индексы соответствуют числам от 0 до n.
// primes[i] = 1, если i — простое число; 0, если составное.
void calculate_primes(int primes[], int n) {
    // Изначально считаем все числа простыми
    for (int i = 0; i <= n; i++) {
        primes[i] = 1;
    }
    // 0 и 1 не являются простыми числами по определению
    primes[0] = primes[1] = 0;

    // Итерация по числам от 2 до sqrt(n)
    for (int i = 2; i * i <= n; i++) {
        if (primes[i]) { // Если текущее число простое
            // Помечаем все кратные числа как составные
            for (int j = i * i; j <= n; j += i) {
                primes[j] = 0;
            }
        }
    }
}
